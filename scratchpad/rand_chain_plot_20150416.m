
function h = rand_chain_plot_20150416

load('../data_store/20150404_183059_rand_chain_int32_50000_500_2147483647.mat');
data4int32	= data;

load('../data_store/20150404_214650_rand_chain_uint32_50000_500_2147483647.mat');
data4uint32	= data;
clear data;

cX	= { 1:data4int32.nT		, ...
		1:data4int32.nT		, ...
		1:data4uint32.nT	, ...
		1:data4uint32.nT	  ...
	  };
cY	= { data4int32.nDupUnchained	, ...
		data4int32.nDupChained		, ...
		data4uint32.nDupUnchained	, ...
		data4uint32.nDupChained		  ...
	  };
cL	= { sprintf('Unchained with %s',data4int32.seedType)	, ...
		sprintf('Chained with %s',data4int32.seedType)		, ...
		sprintf('Unchained with %s',data4uint32.seedType)	, ...
		sprintf('Chained with %s',data4uint32.seedType)		  ...
	  };
assert(data4int32.nSeq==data4uint32.nSeq);
assert(data4int32.range==data4uint32.range);

title	= sprintf(...
			'Collisions across %d Random Integer Sequences with Range %d', ...
			data4int32.nSeq,data4int32.range);
h	= alexplot(cX,cY, ...
		'title'		, title							, ...
		'xlabel'	, 'Time (index into sequence)'	, ...
		'ylabel'	, 'Number of collisions'		, ...
		'legend'	, cL							  ...
		);
set(h.hTitle,'FontSize',14);

end
